package org.linkki.core.message;

/**
 * Enum indicating the kind of severity a message can have
 */

public enum Severity {

    /**
     * Severity none.
     */
    NONE,

    /**
     * Severity info.
     */
    INFO,

    /**
     * Severity warning.
     */
    WARNING,

    /**
     * Severity error.
     */
    ERROR;
}